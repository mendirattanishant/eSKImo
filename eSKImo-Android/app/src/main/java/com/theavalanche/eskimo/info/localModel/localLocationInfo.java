class localLocationInfo{
    int locationID;
    long lat;
    long lon;

    public int getLocationID() {
        return locationID;
    }

    public void setLocationID(int locationID) {
        this.locationID = locationID;
    }

    public void setLat(long lat) {
        this.lat = lat;
    }

    public long getLat() {
        return lat;
    }

    public void setLon(long lon) {
        this.lon = lon;
    }

    public long getLon() {
        return lon;
    }
}